
public class City {
	int cid ;
	String cname ;
	public City(int cid, String cname) {
		super();
		this.cid = cid;
		this.cname = cname;
	}
	public City() {
		super();
	} 
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	
	
	

}
