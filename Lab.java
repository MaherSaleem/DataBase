
public class Lab {
	int lid ;
	int superviseid;
	
	public Lab(int lid, int superviseid) {
		super();
		this.lid = lid;
		this.superviseid = superviseid;
	}
	
	public int getLid() {
		return lid;
	}
	public void setLid(int lid) {
		this.lid = lid;
	}
	public int getSuperviseid() {
		return superviseid;
	}
	public void setSuperviseid(int superviseid) {
		this.superviseid = superviseid;
	}
	

}
